function changeLanguage(lang) {
    // Select all elements that might have translation attributes
    const elements = document.querySelectorAll('[data-i18n], [data-i18n-placeholder], [data-i18n-title], [data-i18n-alt], [data-i18n-aria-label]');

    elements.forEach(el => {
        // 1. Handle main text content (data-i18n)
        if (el.hasAttribute('data-i18n')) {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                if (key === 'hero_title') {
                    el.innerHTML = translations[lang][key].replace(/\n/g, '<br>');
                } else if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    // For inputs, data-i18n might be intended for placeholder if not specified otherwise,
                    // but usually we use data-i18n-placeholder.
                    // If data-i18n is used on input, it might be value? Let's ignore or set value.
                    // safely ignore or set value if needed.
                } else {
                    // Check if element has children (like icons)
                    if (el.children.length > 0) {
                        // If it has children, we try to find a text node or just replace textContent
                        // But replacing textContent wipes children.
                        // Safe approach: if it has children, only replace text nodes?
                        // For this project, we assume simple text replacement or wrapper spans.
                        // If the user put data-i18n on a button with icon, we might lose icon.
                        // We will use a safer approach: find the first text node and replace it, or append if none.
                        // BUT for now, let's stick to textContent as per previous logic, unless we want to be smarter.
                        // Previous logic:
                        // el.textContent = translations[lang][key]; 
                        // This wipes icons.
                        // Let's try to be smarter: find the text node.
                        let textNode = Array.from(el.childNodes).find(node => node.nodeType === Node.TEXT_NODE && node.textContent.trim().length > 0);
                        if (textNode) {
                            textNode.textContent = translations[lang][key];
                        } else {
                            // No text node found, maybe it's empty or only icons.
                            // If we want to force text:
                            // el.textContent = translations[lang][key]; // This wipes icons
                            // Better: create a text node or span?
                            // Let's just fallback to textContent if no children, or if we accept wiping.
                            // Given the codebase, most buttons have <span> for text.
                            // If data-i18n is on the parent button, we should move it to the span.
                            // But if we can't change HTML, we might need to be careful.
                            // For now, let's stick to the previous behavior but maybe warn or handle specific cases.
                            el.textContent = translations[lang][key];
                        }
                    } else {
                        el.textContent = translations[lang][key];
                    }
                }
            }
        }

        // 2. Handle specific attributes
        ['placeholder', 'title', 'alt', 'aria-label'].forEach(attr => {
            const attrKey = el.getAttribute(`data-i18n-${attr}`);
            if (attrKey && translations[lang] && translations[lang][attrKey]) {
                el.setAttribute(attr, translations[lang][attrKey]);
            }
        });
    });

    // Handle Arabic RTL
    if (lang === 'ar') {
        document.documentElement.setAttribute('dir', 'rtl');
        document.documentElement.classList.add('font-arabic');
    } else {
        document.documentElement.setAttribute('dir', 'ltr');
        document.documentElement.classList.remove('font-arabic');
    }

    // Save preference
    localStorage.setItem('preferredLanguage', lang);

    // Update Page Title and Meta Description
    const path = window.location.pathname;
    let pageName = path.substring(path.lastIndexOf('/') + 1);
    if (pageName === '' || pageName === 'index.html') pageName = 'index';
    else pageName = pageName.replace('.html', '').replace(/-/g, '_');

    const titleKey = `${pageName}_page_title`;
    const descKey = `${pageName}_page_desc`;

    if (translations[lang][titleKey]) {
        document.title = translations[lang][titleKey];
    }
    if (translations[lang][descKey]) {
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) {
            metaDesc.setAttribute('content', translations[lang][descKey]);
        }
    }
}

// Load saved language or default to FR
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'fr';
    changeLanguage(savedLang);
});
